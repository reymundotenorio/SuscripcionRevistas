    public void Pagar(){
         int Fila = jTable1.getSelectedRow();
        
  String Suscriptor, Nombres, Apellidos, Revista, ID_Sus;
  int ID;
  double Cuota = 0;

        if (Fila > -1){

            btnmodify.setEnabled(true);
            
            ID_Sus = ((String)this.jTable1.getModel().getValueAt(Fila, 1));
            Nombres = ((String)this.jTable1.getModel().getValueAt(Fila, 3));
            Apellidos = ((String)this.jTable1.getModel().getValueAt(Fila, 4));
            Revista = ((String)this.jTable1.getModel().getValueAt(Fila, 5));
            
            Suscriptor = Nombres.trim().concat(" ")+ Apellidos.trim();
            ID = Integer.parseInt(ID_Sus);
            
            try{
                
                resultado = Conexion.consulta("Select Cuota from cuotav where ID_Suscripcion = "+ID);
                
                while(resultado.next()){
                    
                    Cuota = resultado.getDouble(1);
                }
                
            }catch(SQLException ex){
                
            }
            
            PagoP Pay = new PagoP();
            Pay.setMP(this);
            Pay.Datos(Suscriptor, Revista, String.valueOf(Cuota),"0", ID);
            Pay.setVisible(true);
            
       
        }
    

    }
    
