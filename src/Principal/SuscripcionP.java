    public void Guardar (){
        
        
        
        int Suscriptor, Revista;  
        long Fecha = 0;
        String Sus, Rev;
        
      Sus = (String) cmbSuscriptor.getSelectedItem();
      Rev = (String) cmbRevista.getSelectedItem();
  
      Suscriptor = cmbSuscriptor.getSelectedIndex();
      Revista = cmbRevista.getSelectedIndex();
       
          if("<Seleccione>".equals(Sus))  {
            JOptionPane.showMessageDialog(this,"Seleccione al Suscriptor","Seleccione",JOptionPane.ERROR_MESSAGE);
            return;
           }
        
        if("<Seleccione>".equals(Rev))  {
            JOptionPane.showMessageDialog(this,"Seleccione La Revista","Seleccione",JOptionPane.ERROR_MESSAGE);
            return;
           }
    
          
         if(JdFecha.getDate()!=null){
            
             Date date = JdFecha.getDate();
             Fecha = date.getTime();
           
        }
  
         int ID_S = ID_Suscriptor [Suscriptor];
         int ID_R = ID_Revista [Revista];

            if(Modify==true){

                int ID = ID_Suscripcion;
           
             Suscripcion.Actualizar_Suscripcion(ID, ID_S, ID_R, Fecha);
                
                ActualizarTabla();
                CleanAll();
                DisableAll();
                Modify = false;
            }
            else{

              
              Suscripcion.Agregar_Suscripcion(ID_S, ID_R, Fecha);
                
                
                ActualizarTabla();
                CleanAll();
                DisableAll();

            }
    }
    
