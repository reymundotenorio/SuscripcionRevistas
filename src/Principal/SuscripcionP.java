    public void ActivarDesactivarSuscripcion(){
        int fila = jTable1.getSelectedRow();

        int ID;
        String Nombre;
        int v=JOptionPane.NO_OPTION;
        String Estado;
        String Apellido;
        String Revista;

        if (fila > -1){
            
            

            ID= Integer.parseInt(this.jTable1.getModel().getValueAt(fila, 1).toString());
            Nombre=((String)this.jTable1.getModel().getValueAt(fila, 3));
            Apellido = ((String)this.jTable1.getModel().getValueAt(fila, 4));
            Revista = ((String)this.jTable1.getModel().getValueAt(fila, 5));
            Estado = ((String)this.jTable1.getModel().getValueAt(fila, 6));

           
            
            if("Activo".equalsIgnoreCase(Estado)){
                
                v= JOptionPane.showConfirmDialog(this, "Está Seguro Que Desea Desactivar La Suscripción de "+Nombre.trim()+" "+
                        Apellido.trim()+" Con La Revista "+Revista+"?", "Confirmación", JOptionPane.YES_NO_OPTION);
            }
            if("Inactivo".equalsIgnoreCase(Estado)){
             
                v= JOptionPane.showConfirmDialog(this, "Está Seguro Que Desea Activar La Suscripción de "+Nombre.trim()+" "+
                        Apellido.trim()+" Con La Revista "+Revista+"?", "Confirmación", JOptionPane.YES_NO_OPTION);
            }

            if(v==JOptionPane.YES_OPTION){

                
                Suscripcion.Activar_Desactivar_Suscripcion(ID);
               
                ActualizarTabla();
            }
        }

        else{

            JOptionPane.showMessageDialog(this, "Seleccione Una Suscripción a Activar/Desactivar", "Error", JOptionPane.ERROR_MESSAGE);

        }

    }
    
    
