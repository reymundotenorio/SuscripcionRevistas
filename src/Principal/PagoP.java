    public void Guardar (){
        
        
        
        int NoCuotas;  
        long Fecha = 0;
        String Cuotas;
        
        Cuotas = txtNoCuotas.getText().trim();
 
        
        if("".equals(Cuotas))  {
            JOptionPane.showMessageDialog(this,"Seleccione La Revista","Seleccione",JOptionPane.ERROR_MESSAGE);
            return;
           }
        
        int SumCuota = 0;
        
        try{
            
            resultado = Conexion.consulta("Select Sum(No_Cuotas) from pago where ID_Suscripcion = "+ID_Suscripcion);
            
            while(resultado.next()){
                SumCuota = resultado.getInt(1);
            }
            
        }catch(SQLException ex){
            
        }
        
        if(Modify==true){
            SumCuota=SumCuota+ViejaCuota;
        }
        
        int SC = 11-SumCuota;
       
    
        NoCuotas = Integer.parseInt(Cuotas);
        

          if(11==SumCuota){
              JOptionPane.showMessageDialog(this, "La Suscripci√≥n Ha Sido Pagada Por Completo","Pagada",JOptionPane.WARNING_MESSAGE);     
             return; 
          }
        
          if(SC<NoCuotas)  {
          JOptionPane.showMessageDialog(this, "El N√∫mero de Cuota a Pagar Excede La Cantidad Permitido","Exceso",JOptionPane.ERROR_MESSAGE);     
             return; 
         }
          
        if(NoCuotas>11){
             JOptionPane.showMessageDialog(this, "El N√∫mero de Cuota a Pagar Excede La Cantidad Permitido","Exceso",JOptionPane.ERROR_MESSAGE);     
             return; 
         }
          
         if(JdFecha.getDate()!=null){
            
             Date date = JdFecha.getDate();
             Fecha = date.getTime();
           
        }
  
         if(Modify == true){
             
             Pago.Actualizar_Pago(ID_Suscripcion, NoCuotas, Fecha);
             Modify = false;
             
         }
         else{

              
                Pago.Agregar_Pago(ID_Suscripcion, NoCuotas, Fecha);
         }
                CleanAll();
                DisableAll();
                
                dispose();

            
    }
    
