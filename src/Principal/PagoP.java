    private void txtCuotaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCuotaKeyTyped
        char a = evt.getKeyChar();
        char c = evt.getKeyChar();

        double x=0;

        if("0".equals(txtCuota.getText())&&txtCuota.getCaretPosition()==1&&c!='.'&&!Character.isISOControl(c)){
            txtCuota.setText(txtCuota.getText()+".");
            //   return;
        }

        if (c == '.' && txtCuota.getText().contains(".")) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
            return;
        }

        String x1 = "";
        if(Character.isDigit(c)||(Character.isISOControl(c)&&!"".equals(txtCuota.getText()))){
            x1 = txtCuota.getText();
        }
        if(Character.isDigit(c)||(c == '.')){
            x1 = txtCuota.getText().concat(String.valueOf(c));
        }

        if(!"".equals(x1)){
            x = Double.parseDouble(x1);
        }

        if ((!Character.isDigit(c)) && (!Character.isISOControl(c))
            && (c != '.')||x>99999999) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();

        }        // TODO add your handling code here:

        // TODO add your handling code here:
    }//GEN-LAST:event_txtCuotaKeyTyped

